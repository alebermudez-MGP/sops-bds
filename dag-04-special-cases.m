## Section 3 — Collection Review

<details>
<summary>📂 Step 1 — Confirm file status</summary>
<br>
Verify the file status is Collection Review before proceeding.
</details>

<details>
<summary>✉️ Step 2 — Send email to Sales Agent</summary>
<br>
Confirm whether the merchant intends to continue with the file.
<br><br>
<strong>If continuing:</strong>
<ol>
<li>The merchant must be brought back on track.</li>
<li>UW emails CS requesting assistance, advising the merchant to follow up with the BDN Sales Agent.</li>
<li>Once back on track, either UW or CS may update status/reactivate transactions.</li>
</ol>
💡 CS email reference — DAG: <strong>clientsupport@dag-law.com</strong> · BDS: <strong>clientsupport@businessdebtsolutions.com</strong>
<br><br>
<strong>If not continuing:</strong>
<ol>
<li>Create a new opportunity following the established process and proper pricing.</li>
<li>Once the program cost matches the calculator, add a 2-month add-on fee, increasing the program cost.</li>
</ol>
⚠️ If the merchant already paid substantial fees and Sales submits a new request, consult your Supervisor/Manager for a possible exception.
</details>

