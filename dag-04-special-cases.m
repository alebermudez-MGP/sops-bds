## Section 4 — Portfolio Switches

<details>
<summary>🔄 Correct process for merchant portfolio switches</summary>
<br>
To switch from one portfolio to another (BDS or DAG), follow these steps to ensure clean documentation.
<br><br>
<strong>Close the original portfolio file:</strong>
<ol>
<li>Update the Underwriting Status to <strong>Withdrawn</strong>.</li>
<li>If a contract was already sent, void it immediately.</li>
<li>Add an internal note with the exact text below.</li>
</ol>

```
Client does not want to continue with this plan.
```

<strong>Start work under the new assigned portfolio:</strong>
<ol start="4">
<li>Begin processing the file under the new portfolio as recommended by management.</li>
<li>Continue following the standard underwriting (UW) steps as usual.</li>
</ol>
💡 The Underwriting stage and Withdrawn status continue to be used when switching an opportunity to the other portfolio.
</details>

