## Section 2 — Delinquency

<details>
<summary>📌 Delinquency Status categories</summary>
<br>
<ul>
<li>Information Collection <em>(check with management before proceeding with Delinquent)</em></li>
<li>Program Hold / Paused <em>(check with management before proceeding with Delinquent)</em></li>
<li>Cancellation Requested <em>(check with management before proceeding with Delinquent)</em></li>
<li>Cancellation Approved</li>
<li>Cancellation Processed</li>
<li>Need to Process Refund</li>
<li>Refund Processed</li>
<li>Client Defaulted</li>
<li>Cancellation Fee Paid</li>
<li>Client Canceled</li>
<li>Canceled</li>
<li>Collections Review</li>
</ul>
</details>

<details>
<summary>🚫 No double delinquency rule</summary>
<br>
We will <strong>no longer enroll any file that has been delinquent more than once</strong>, regardless of whether it occurred under the same business name or a different business name with the same owner.
<br><br>
If the same owner has a different business name and one or more accounts are in "Collections Review" or have unresolved recent returns, we may proceed under the new business name — but the merchant must submit a CC payment for the required fees first.
<br><br>
Add notes:
```
Merchant must make a payment for the Max CC Payment Amount ($X.XX) to be enrolled.
```
and inform Sales.
</details>

<details>
<summary>📝 UW Guidelines for delinquent accounts</summary>
<br>
<ol>
<li><strong>Review for duplicates</strong> — check notes on any delinquent-status account before deciding or asking Sales for clarification.</li>
<li><strong>Verify the cancellation fee</strong> — add up all noted amounts (may be under Management MGP or a manager).</li>
<li><strong>Check enrollment plans</strong> — confirm whether any transaction has cleared (fees already paid).</li>
<li><strong>Update pricing if no fees paid</strong> — add the total cancellation fee to the program payment amount on the DAG pricing check for the new opportunity.</li>
<li><strong>Create a new opportunity</strong> — reuse banking information and VC from the previous account. Leave detailed notes.</li>
</ol>
</details>

