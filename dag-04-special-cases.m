## 🚫 Not applicable to DAG

<details>
<summary>📆 Move Payment Requests (MPR)</summary>
<br>
DAG does not have a Move Payment Request process. If a request to move a scheduled payment comes in for a DAG file, escalate to Management — do not process it using the BDS MPR steps.
</details>

