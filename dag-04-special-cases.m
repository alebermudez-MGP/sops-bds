# ⚠️ DAG 04 — Special Cases

Reference this page when a file involves Concurrent accounts, Delinquency, Collection Review, Portfolio switches, Re-enrollment, Bank Account Updates, or Puerto Rico files.

---

## Section 1 — Concurrent Accounts

<details>
<summary>➕ When a Concurrent should be created</summary>
<br>
A Concurrent is created when:
<ul>
<li>The same business name and same owner are involved,</li>
<li>But the creditor is different.</li>
</ul>
If the creditor is the <strong>same</strong>, confirm with Sales whether this is a second position to avoid creating a duplicate account for an existing lender.
<br><br>
In many cases, merchants request to add a new creditor to the program after they are already enrolled and have made one or more payments. Before creating a Concurrent, review all of the following items carefully.
</details>

<details>
<summary>✅ 1. Enrolled Account</summary>
<br>
A Concurrent should only be created if the original account is already enrolled.
</details>

<details>
<summary>📅 2. 1st Payment Status</summary>
<br>
<ul>
<li><strong>If the 1st payment is in Scheduled status:</strong>
<ul>
<li>Attempt to avoid creating a Concurrent.</li>
<li>Modify the current account instead and send a new contract with the same FPD.</li>
<li>Remember to check the cutoff time.</li>
</ul>
</li>
<li><strong>If the 1st payment is in Pending or Cleared status:</strong>
<ul><li>You must proceed with a Concurrent.</li></ul>
</li>
<li><strong>If a CC payment is made before the scheduled 1st payment:</strong>
<ul><li>You may proceed with a Concurrent only if the opportunity notes confirm a Secured CC Payment, or the Card Payment Authorization Form is signed and uploaded.</li></ul>
</li>
</ul>
</details>

<details>
<summary>↩️ 3. Recent Returns</summary>
<br>
<ul>
<li>If there are recent returns, the file may need to be handled as a delinquency instead of a Concurrent.</li>
<li>Review the file and notes thoroughly to confirm whether the return has been rescheduled.</li>
<li>The merchant must address the return(s) or the account must show a confirmed reschedule before you proceed.</li>
<li>If the above is met, you may continue with the Concurrent.</li>
</ul>
</details>

<details>
<summary>🕐 4. 1st Payment Date in Scheduled Status (Same-Day Batch)</summary>
<br>
If the batch date is the same day and we are before cutoff:
<ul>
<li>Inform the Sales Agent a Concurrent cannot be created yet.</li>
<li>Options: (1) send a new contract with updated creditor info keeping the same FPD, or (2) wait until after cutoff and proceed with the Concurrent.</li>
</ul>
</details>

<details>
<summary>⚠️ 5. Recent Returns Not Cleared or Rescheduled</summary>
<br>
If unresolved, notify the Sales Agent the file must be handled by the CS Team. Once the account returns to ACTIVE status, you may proceed with the Concurrent request.
</details>

<details>
<summary>📋 Concurrent Requirements</summary>
<br>
<ul>
<li>Any concurrent file must be handled under the same portfolio as the original file. Example: if a merchant has Business #1 with one lender under DAG, and later enrolls Business #1 with a different lender as a concurrent file, the concurrent file must also be completed under DAG.</li>
<li>Assign the same client service agent already on the original account.</li>
<li>Concurrents do not apply to different debts — only when the debt type is the same within the same portfolio.</li>
</ul>
<br>
📩 <strong>QA Reminder:</strong> To avoid errors, carefully verify the debt type and portfolio before creating or assigning a file as concurrent. Double-check this information to ensure accuracy in our processes.
</details>

