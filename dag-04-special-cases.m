## Section 5 — Re-enrollment

<details>
<summary>🔁 Re-enrolling a merchant after a withdrawn or canceled file</summary>
<br>
Treat it as a new opportunity rather than reactivating the old one — create a new opportunity with proper pricing, and once the program cost matches the calculator, add a 2-month add-on fee.
<br><br>
⚠️ If the merchant already paid substantial fees previously, consult your Supervisor/Manager — an exception may apply.
</details>

